public class Vector2 {
    public static class Int32 {
        public int x;
        public int y;
        public Int32(int x, int y) {
            this.x = x;
            this.y = y;
        }
    }
    public static class Float32 {
        public float x;
        public float y;
        public Float32(int x, int y) {
            this.x = x;
            this.y = y;
        }
    }
}
